    private void habilitarBotaoConcluir(){
        if(jtNome.getText().length()>2 && jtBi.getText().length()>2 && 
                jtNacionalidade.getText().length()>2 && jtNaturalidade.getText().length()>2 &&
                jtLocalDeResidencia.getText().length()>2 && (jrMasculino.isSelected() || 
                jrFeminino.isSelected()) && jrDoencaSim.isSelected() && jtDoenca.getText().length()>2
                && jtaCuidados.getText().length()>2 && jrCrimeSim.isSelected() && jtaCrimes.getText().length()>2){
                jbRegistar.setEnabled(true);
        } 
        if(jtNome.getText().length()>2 && jtBi.getText().length()>2 && 
                jtNacionalidade.getText().length()>2 && jtNaturalidade.getText().length()>2 &&
                jtLocalDeResidencia.getText().length()>2 && (jrMasculino.isSelected() || 
                jrFeminino.isSelected()) && jrDoencaNao.isSelected() &&
                jrCrimeNao.isSelected()){
                jbRegistar.setEnabled(true);
        }
        if(jtNome.getText().length()<3 || jtBi.getText().length()<3 || 
                jtNacionalidade.getText().length()<3 || jtNaturalidade.getText().length()<3 ||
                jtLocalDeResidencia.getText().length()<3){
                jbRegistar.setEnabled(false);
        }
        
        if((jrCrimeSim.isSelected() && jtaCrimes.getText().length()<3) || 
                (jrDoencaSim.isSelected() && jtDoenca.getText().length()<3 && 
                jtaCuidados.getText().length()<3)){
            jbRegistar.setEnabled(false);
        }
        
    }

